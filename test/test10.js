function binary_op(a, b) {
	print(a + b);
	print(a - b);
	print(a * b);
	print(a / b);
	print(a % b);
	print(a | b);
	print(a & b);
	print(a << b);
	print(a >> b);
}

function binary_test(a, b) {
	print(a == b);
	print(a != b);
	print(a < b);
	print(a <= b);
	print(a > b);
	print(a >= b);
}

binary_op(2, 2);
binary_test(2, 2);


